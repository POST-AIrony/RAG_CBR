from bs4 import BeautifulSoup, SoupStrainer

soup = BeautifulSoup(
    open("./analytics.html", "r", encoding="utf-8").read(), "html.parser"
)
docs = soup.find_all("a", class_="versions_item")
data = []
c = 1
for i in docs:
    print(i)
    title = i["data-zoom-referer-title"]
    url = i["href"]
    try:
        num, date = i.text.split("")
    except:
        num = date = " "
    data.append({"title": title, "utl": url, "number": num, "date": date, "id": c})
    c += 1

import json

with open("analytics.json", "w", encoding="utf-8") as fp:
    json.dump(data, fp, ensure_ascii=False)
